package learnjava2023;

public class Conditionals {

	public static void main(String[] args) {
		
		int number = 24;
		
		if(number > 20) {
			
			System.out.println("Number is bigger than 20");
		
		}else if(number == 20){
			
			System.out.println("Number is equal to 20");
			
		}else {
			
			System.out.println("Number lower than 20");
			
		}
	}

}
